#slicing/Selecting sub sequences
lang="python"

#Syntax - [start argument : Stop argument -1 : step]
 
print("Siddhartha"[0 : 5 : 2])
#only start and step
print("Siddhartha"[0 :  : 2])
#only stop and step
print("Siddhartha"[ : 5 : 2])
#Empty
print("Siddhartha"[ :  : 2])
#Reverse
print("Siddhartha"[9 :  : -2])

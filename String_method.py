#String method part one
name = "Siddhartha Singh"
#1. len()function #its use to count words 
print(len("Siddhartha"))
print(len(name))# in this count spaces also
count = len(name)
print(count)
#2. lower()method #its use to make all words in small letters
print(name.lower())

#3. Upper()method #its use to make all words upper
print(name.upper())

#4. tilte()method #it make all title upper
print(name.title())

#5. Count()method #it count the single word in the words
print(name.count("h"))
